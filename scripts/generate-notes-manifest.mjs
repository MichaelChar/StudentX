#!/usr/bin/env node
/**
 * Generate src/lib/notes/manifest.generated.js from the JSON content tree.
 *
 * WHY THIS EXISTS — Cloudflare Workers (OpenNext) has no runtime `fs`, so the
 * loaders in src/lib/notes/content.js cannot read content/*.json at request
 * time. Instead we emit a module of **static imports** of every notes JSON
 * file. The bundler inlines those imports, so the data ships inside the Worker
 * bundle and the loaders are pure synchronous lookups — no fs, works anywhere.
 * Mirrors scripts/generate-practice-manifest.mjs.
 *
 * Each subject is a single file: content/notes/ausom/<semester-N>/<subject>.json
 * (one NotesDoc per subject — no index.json, unlike practice).
 *
 * Run this whenever content under content/notes/ changes, then commit the
 * regenerated manifest:
 *
 *   npm run notes:manifest
 *
 * This script runs in Node at author/CI time and may use fs freely.
 */

import { readdirSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
// Walk every semester under the AUSoM tree: content/notes/ausom/<semester-N>/<subject>.json.
const CONTENT_ROOT = path.join(ROOT, 'content/notes/ausom');
const OUT_FILE = path.join(ROOT, 'src/lib/notes/manifest.generated.js');

/** Directory names that look like a semester slug ("semester-6"), sorted. */
function semesterDirs(root) {
  return existsSync(root)
    ? readdirSync(root, { withFileTypes: true })
        .filter((d) => d.isDirectory() && /^semester-\d+$/.test(d.name))
        .map((d) => d.name)
        .sort()
    : [];
}

/** kebab/anything -> a safe JS identifier fragment, e.g. "hygiene-epidemiology" -> "hygieneEpidemiology". */
function ident(...parts) {
  const raw = parts.join('_').replace(/[^a-zA-Z0-9]+/g, '_');
  return raw.replace(/^_+/, '').replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase());
}

/** Import path, relative to OUT_FILE, with forward slashes for the bundler. */
function importPath(absJsonPath) {
  const rel = path.relative(path.dirname(OUT_FILE), absJsonPath);
  const norm = rel.split(path.sep).join('/');
  return norm.startsWith('.') ? norm : `./${norm}`;
}

function main() {
  const imports = [];
  const semesterEntries = [];
  let docCount = 0;

  for (const semester of semesterDirs(CONTENT_ROOT)) {
    const semesterDir = path.join(CONTENT_ROOT, semester);
    const files = readdirSync(semesterDir)
      .filter((f) => f.endsWith('.json'))
      .sort();

    const subjectEntries = [];
    for (const file of files) {
      const subject = file.replace(/\.json$/, '');
      const docVar = ident(semester, subject, 'notes');
      imports.push(`import ${docVar} from '${importPath(path.join(semesterDir, file))}';`);
      subjectEntries.push(`    ${JSON.stringify(subject)}: ${docVar},`);
      docCount += 1;
    }

    if (subjectEntries.length === 0) continue;
    semesterEntries.push(
      `  ${JSON.stringify(semester)}: {\n${subjectEntries.join('\n')}\n  },`,
    );
  }

  const body = `// @generated by scripts/generate-notes-manifest.mjs — DO NOT EDIT BY HAND.
// Run \`npm run notes:manifest\` to regenerate after changing content/notes/.
//
// Static imports make notes JSON part of the bundle, so the loaders in
// content.js need no runtime fs (required for Cloudflare Workers / OpenNext).

${imports.join('\n')}

/** @type {Record<string, Record<string, import('./schema.js').NotesDoc>>} */
export const MANIFEST = {
${semesterEntries.join('\n')}
};
`;

  writeFileSync(OUT_FILE, body);
  console.log(
    `Wrote ${path.relative(ROOT, OUT_FILE)} — ${semesterEntries.length} semester(s), ${docCount} notes doc(s).`,
  );
}

main();
