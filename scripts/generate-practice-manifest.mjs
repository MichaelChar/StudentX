#!/usr/bin/env node
/**
 * Generate src/lib/practice/manifest.generated.js from the JSON content tree.
 *
 * WHY THIS EXISTS — Cloudflare Workers (OpenNext) has no runtime `fs`, so the
 * loaders in src/lib/practice/content.js cannot read content/*.json at request
 * time. Instead we emit a module of **static imports** of every content JSON
 * file. The bundler inlines those imports, so the data ships inside the Worker
 * bundle and the loaders are pure synchronous lookups — no fs, works anywhere.
 *
 * Run this whenever content under content/practice/ changes (e.g. after the
 * generate-practice-test skill adds a subject), then commit the regenerated
 * manifest:
 *
 *   npm run practice:manifest
 *
 * This script runs in Node at author/CI time and may use fs freely.
 */

import { readFileSync, readdirSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CONTENT_ROOT = path.join(ROOT, 'content/practice/ausom/semester-2');
const OUT_FILE = path.join(ROOT, 'src/lib/practice/manifest.generated.js');

/** kebab/anything -> a safe JS identifier fragment, e.g. "anatomy-1" -> "anatomy1". */
function ident(...parts) {
  const raw = parts.join('_').replace(/[^a-zA-Z0-9]+/g, '_');
  return raw.replace(/^_+/, '').replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase());
}

/** Import path, relative to OUT_FILE, with forward slashes for the bundler. */
function importPath(absJsonPath) {
  const rel = path.relative(path.dirname(OUT_FILE), absJsonPath);
  const norm = rel.split(path.sep).join('/');
  return norm.startsWith('.') ? norm : `./${norm}`;
}

function main() {
  if (!existsSync(CONTENT_ROOT)) {
    console.error(`No content directory at ${CONTENT_ROOT}`);
    process.exit(1);
  }

  const subjects = readdirSync(CONTENT_ROOT, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  const imports = [];
  const entries = [];

  for (const subject of subjects) {
    const dir = path.join(CONTENT_ROOT, subject);
    const indexPath = path.join(dir, 'index.json');
    if (!existsSync(indexPath)) {
      console.warn(`Skipping "${subject}": no index.json`);
      continue;
    }

    const indexVar = ident(subject, 'index');
    imports.push(`import ${indexVar} from '${importPath(indexPath)}';`);

    const testFiles = readdirSync(dir)
      .filter((f) => f.endsWith('.json') && f !== 'index.json')
      .sort();

    const testEntries = [];
    for (const file of testFiles) {
      const testId = file.replace(/\.json$/, '');
      const testVar = ident(subject, 'test', testId);
      imports.push(`import ${testVar} from '${importPath(path.join(dir, file))}';`);
      testEntries.push(`      ${JSON.stringify(testId)}: ${testVar},`);
    }

    entries.push(
      `  ${JSON.stringify(subject)}: {\n` +
        `    index: ${indexVar},\n` +
        `    tests: {\n${testEntries.join('\n')}\n    },\n` +
        `  },`,
    );
  }

  const body = `// @generated by scripts/generate-practice-manifest.mjs — DO NOT EDIT BY HAND.
// Run \`npm run practice:manifest\` to regenerate after changing content/practice/.
//
// Static imports make practice-test JSON part of the bundle, so the loaders in
// content.js need no runtime fs (required for Cloudflare Workers / OpenNext).

${imports.join('\n')}

/** @type {Record<string, { index: import('./schema.js').SubjectIndex, tests: Record<string, import('./schema.js').PracticeTest> }>} */
export const MANIFEST = {
${entries.join('\n')}
};
`;

  writeFileSync(OUT_FILE, body);
  const testCount = entries.length
    ? imports.length - subjects.filter((s) => existsSync(path.join(CONTENT_ROOT, s, 'index.json'))).length
    : 0;
  console.log(
    `Wrote ${path.relative(ROOT, OUT_FILE)} — ${entries.length} subject(s), ${testCount} test(s).`,
  );
}

main();
