#!/usr/bin/env node
/**
 * Generate src/lib/flashcards/manifest.generated.js from the JSON content tree.
 *
 * WHY THIS EXISTS — Cloudflare Workers (OpenNext) has no runtime `fs`, so the
 * loaders in src/lib/flashcards/content.js cannot read content/*.json at
 * request time. Instead we emit a module of **static imports** of every
 * content JSON file. The bundler inlines those imports, so the data ships
 * inside the Worker bundle and the loaders are pure synchronous lookups — no
 * fs, works anywhere. Mirrors scripts/generate-practice-manifest.mjs.
 *
 * Run this whenever content under content/flashcards/ changes, then commit
 * the regenerated manifest:
 *
 *   npm run flashcards:manifest
 *
 * This script runs in Node at author/CI time and may use fs freely.
 */

import { readdirSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CONTENT_ROOT = path.join(ROOT, 'content/flashcards');
const OUT_FILE = path.join(ROOT, 'src/lib/flashcards/manifest.generated.js');

/** kebab/anything -> a safe JS identifier fragment, e.g. "general-histology" -> "generalHistology". */
function ident(...parts) {
  const raw = parts.join('_').replace(/[^a-zA-Z0-9]+/g, '_');
  return raw.replace(/^_+/, '').replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase());
}

/** Import path, relative to OUT_FILE, with forward slashes for the bundler. */
function importPath(absJsonPath) {
  const rel = path.relative(path.dirname(OUT_FILE), absJsonPath);
  const norm = rel.split(path.sep).join('/');
  return norm.startsWith('.') ? norm : `./${norm}`;
}

function main() {
  const subjects = existsSync(CONTENT_ROOT)
    ? readdirSync(CONTENT_ROOT, { withFileTypes: true })
        .filter((d) => d.isDirectory())
        .map((d) => d.name)
        .sort()
    : [];

  const imports = [];
  const entries = [];

  for (const subject of subjects) {
    const dir = path.join(CONTENT_ROOT, subject);
    const indexPath = path.join(dir, 'index.json');
    if (!existsSync(indexPath)) {
      console.warn(`Skipping "${subject}": no index.json`);
      continue;
    }

    const indexVar = ident(subject, 'index');
    imports.push(`import ${indexVar} from '${importPath(indexPath)}';`);

    entries.push(`  ${JSON.stringify(subject)}: {\n` + `    index: ${indexVar},\n` + `  },`);
  }

  const body = `// @generated by scripts/generate-flashcards-manifest.mjs — DO NOT EDIT BY HAND.
// Run \`npm run flashcards:manifest\` to regenerate after changing content/flashcards/.
//
// Static imports make flashcards-subject JSON part of the bundle, so the
// loaders in content.js need no runtime fs (required for Cloudflare Workers /
// OpenNext).

${imports.join('\n')}

/** @type {Record<string, { index: import('./schema.js').SubjectIndex }>} */
export const MANIFEST = {
${entries.join('\n')}
};
`;

  writeFileSync(OUT_FILE, body);
  console.log(`Wrote ${path.relative(ROOT, OUT_FILE)} — ${entries.length} subject(s).`);
}

main();
